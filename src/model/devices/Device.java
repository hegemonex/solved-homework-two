package model.devices;

public class Device {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String deviceInfo(){
        return "This is " + name + " with id " + id;
    }
}
